function cleanGeneratedSweepFixtures() {
  ensureDir(VECTOR_DIR);
  for (const file of listVectorFiles()) {
    const name = path.basename(file);
    if (name.startsWith("edge_") || name.startsWith("random_") || name.startsWith("exhaustive_")) {
      fs.unlinkSync(file);
    }
  }
  if (exists(SWEEP_TEST_PATH)) {
    fs.unlinkSync(SWEEP_TEST_PATH);
  }
}

function cartesianPairs(values) {
  const rows = [];
  for (const a of values) {
    for (const b of values) {
      rows.push([a, b]);
    }
  }
  return rows;
}

function bits(rng, fmt) {
  const [width, digits] = FORMATS[fmt];
  const mask = (1n << BigInt(width)) - 1n;
  return hex(rng.next() & mask, digits);
}

function modeCycle(index) {
  return [
    ROUNDINGS[index % ROUNDINGS.length],
    TININESS[Math.floor(index / ROUNDINGS.length) % TININESS.length],
    "1",
  ];
}

function allModeRows(inputs) {
  const rows = [];
  for (const rounding of ROUNDINGS) {
    for (const tininess of TININESS) {
      for (const values of inputs) {
        rows.push([rounding, tininess, "1", values]);
      }
    }
  }
  return rows;
}

function allRoundingRows(inputs) {
  const rows = [];
  for (const rounding of ALL_ROUNDINGS) {
    for (const exact of ["0", "1"]) {
      for (const values of inputs) {
        rows.push([rounding, "after", exact, values]);
      }
    }
  }
  return rows;
}

function cycledRandomRows(rng, fmt, arity, count) {
  const rows = [];
  for (let index = 0; index < count; index += 1) {
    const [rounding, tininess, exact] = modeCycle(index);
    const values = [];
    for (let item = 0; item < arity; item += 1) {
      values.push(bits(rng, fmt));
    }
    rows.push([rounding, tininess, exact, values]);
  }
  return rows;
}

function batchRecords(spec, op, rows) {
  // The C oracle supports a batch mode keyed by rounding/tininess/exact. Grouping
  // rows this way keeps generation fast while preserving the original row order.
  const groups = new Map();
  rows.forEach(([rounding, tininess, exact, inputs], index) => {
    const key = `${rounding}\0${tininess}\0${exact}`;
    if (!groups.has(key)) {
      groups.set(key, []);
    }
    groups.get(key).push([index, inputs]);
  });

  const records = Array(rows.length).fill(null);
  for (const key of [...groups.keys()].sort()) {
    const [rounding, tininess, exact] = key.split("\0");
    const batch = groups.get(key);
    const input = batch.map(([, inputs]) => inputs.join(" ")).join("\n") + "\n";
    const completed = runCommand(
      [oraclePath(spec), "--batch", op, rounding, tininess, exact],
      { input, captureOutput: true },
    );
    const actual = completed.stdout.split(/\r?\n/).filter((line) => line.length > 0);
    if (actual.length !== batch.length) {
      fail(`${op}/${spec}: oracle returned the wrong record count`);
    }
    batch.forEach(([index], offset) => {
      records[index] = actual[offset];
    });
  }
  return records.filter((record) => record !== null);
}

function mixDigest(value, digest) {
  return ((digest ^ (value & MASK64)) * HASH_PRIME) & MASK64;
}

function digestRecords(records) {
  // The digest deliberately folds only the oracle output and exception flags.
  // Inputs are reproduced by the generated MoonBit code; mismatched input order
  // changes the output stream and therefore the digest.
  let digest = HASH_INIT;
  for (const record of records) {
    const fields = record.split(/\s+/);
    digest = mixDigest(parseHex(fields.at(-2)), digest);
    digest = mixDigest(parseHex(fields.at(-1)), digest);
  }
  return digest;
}

function writeFixture(name, spec, op, rows, source) {
  const file = path.join(VECTOR_DIR, `${name}_${op}_${spec}.txt`);
  const records = batchRecords(spec, op, rows);
  writeText(file, records.join("\n") + "\n");
  DIGEST_CASES.push(new DigestCase({
    name: `${name}_${op}_${spec}`,
    spec,
    op,
    source,
    digest: digestRecords(records),
  }));
}

function edgeBinaryRows(fmt) {
  return allModeRows(cartesianPairs(EDGE_VALUES[fmt]));
}

function edgeFmaRows(fmt) {
  const values = EDGE_VALUES[fmt];
  const triples = [];
  values.forEach((a, i) => {
    values.forEach((b, j) => {
      triples.push([a, b, values[(i * 7 + j * 3) % values.length]]);
    });
  });
  return allModeRows(triples);
}

function generateEdgeVectors() {
  for (const spec of SPECS) {
    for (const [fmt, ops] of Object.entries(BINARY_OPS)) {
      const rows = edgeBinaryRows(fmt);
      for (const op of ops) {
        writeFixture(`edge_${fmt}`, spec, op, rows, { kind: "edge_binary", fmt });
      }
    }
    for (const [fmt, op] of Object.entries(FMA_OPS)) {
      writeFixture(`edge_${fmt}`, spec, op, edgeFmaRows(fmt), { kind: "edge_fma", fmt });
    }
    for (const [fmt, ops] of Object.entries(FLOAT_TO_INT_OPS)) {
      const rows = allRoundingRows(EDGE_VALUES[fmt].map((value) => [value]));
      for (const op of ops) {
        writeFixture(`edge_${fmt}`, spec, op, rows, { kind: "edge_unary_all_rounding", fmt });
      }
    }
    for (const [fmt, ops] of Object.entries(MIN_MAG_OPS)) {
      const rows = [];
      for (const exact of ["0", "1"]) {
        for (const value of EDGE_VALUES[fmt]) {
          rows.push(["near_even", "after", exact, [value]]);
        }
      }
      for (const op of ops) {
        writeFixture(`edge_${fmt}`, spec, op, rows, { kind: "edge_unary_min_mag", fmt });
      }
    }
    for (const [fmt, op] of Object.entries(ROUND_TO_INT_OPS)) {
      writeFixture(
        `edge_${fmt}`,
        spec,
        op,
        allRoundingRows(EDGE_VALUES[fmt].map((value) => [value])),
        { kind: "edge_unary_all_rounding", fmt },
      );
    }
    for (const [fmt, ops] of Object.entries(FLOAT_CONVERSION_OPS)) {
      const rows = allModeRows(EDGE_VALUES[fmt].map((value) => [value]));
      for (const op of ops) {
        writeFixture(`edge_${fmt}`, spec, op, rows, { kind: "edge_unary_all_mode", fmt });
      }
    }
    for (const [inputKind, op] of INT_TO_FLOAT_OPS) {
      const rows = allRoundingRows(INTEGER_INPUTS[inputKind].map((value) => [value]));
      writeFixture(`edge_${inputKind}`, spec, op, rows, { kind: "edge_int_all_rounding", inputKind });
    }
    for (const op of ["bf16_to_f32", "bf16_is_signaling_nan"]) {
      const rows = EDGE_VALUES.f16.map((value) => ["near_even", "after", "1", [value.slice(0, 4)]]);
      writeFixture("edge_bf16", spec, op, rows, { kind: "edge_bf16" });
    }
  }
}

function generateRandomVectors(args) {
  const rng = new Lcg(SEED);
  const counts = {
    f16: args.f16BinaryCount,
    f32: args.f32BinaryCount,
    f64: args.f64BinaryCount,
  };
  for (const spec of SPECS) {
    for (const [fmt, ops] of Object.entries(BINARY_OPS)) {
      for (const op of ops) {
        const seed = rng.state;
        writeFixture(
          `random_${fmt}`,
          spec,
          op,
          cycledRandomRows(rng, fmt, 2, counts[fmt]),
          { kind: "random", fmt, arity: 2, count: counts[fmt], seed },
        );
      }
    }
    for (const [fmt, op] of Object.entries(FMA_OPS)) {
      const seed = rng.state;
      writeFixture(
        `random_${fmt}`,
        spec,
        op,
        cycledRandomRows(rng, fmt, 3, args.fmaCount),
        { kind: "random", fmt, arity: 3, count: args.fmaCount, seed },
      );
    }
    const seed = rng.state;
    writeFixture(
      "random_f32",
      spec,
      "f32_to_bf16",
      cycledRandomRows(rng, "f32", 1, args.f32BinaryCount),
      { kind: "random", fmt: "f32", arity: 1, count: args.f32BinaryCount, seed },
    );
  }
}

function generateExhaustiveF16Vectors() {
  const rows = [];
  for (let value = 0; value < 0x10000; value += 1) {
    rows.push(["near_even", "after", "1", [value.toString(16).toUpperCase().padStart(4, "0")]]);
  }
  const roundRows = [];
  for (const rounding of ["near_even", "min", "max", "odd"]) {
    for (let value = 0; value < 0x10000; value += 1) {
      roundRows.push([rounding, "after", "1", [value.toString(16).toUpperCase().padStart(4, "0")]]);
    }
  }
  for (const spec of SPECS) {
    for (const op of ["f16_to_f32", "f16_to_f64", "f16_is_signaling_nan"]) {
      writeFixture("exhaustive_f16", spec, op, rows, { kind: "exhaustive_f16_unary" });
    }
    writeFixture(
      "exhaustive_f16",
      spec,
      "f16_round_to_int",
      roundRows,
      { kind: "exhaustive_f16_round_to_int" },
    );
  }
}

function generateExhaustiveBf16Vectors() {
  const rows = [];
  for (let value = 0; value < 0x10000; value += 1) {
    rows.push(["near_even", "after", "1", [value.toString(16).toUpperCase().padStart(4, "0")]]);
  }
  for (const spec of SPECS) {
    for (const op of ["bf16_to_f32", "bf16_is_signaling_nan"]) {
      writeFixture("exhaustive_bf16", spec, op, rows, { kind: "exhaustive_bf16_unary" });
    }
  }
}

function isPredicate(op) {
  return op.endsWith("_is_signaling_nan");
}

function isBoolResult(op) {
  return isPredicate(op)
    || op.endsWith("_eq")
    || op.endsWith("_le")
    || op.endsWith("_lt")
    || op.endsWith("_eq_signaling")
    || op.endsWith("_le_quiet")
    || op.endsWith("_lt_quiet");
}

function isFloatToInt(op) {
  return ["_to_ui32", "_to_ui64", "_to_i32", "_to_i64"].some((token) => op.includes(token))
    && !op.startsWith("ui")
    && !op.startsWith("i");
}

function needsRoundingArg(op) {
  return op.endsWith("_round_to_int") || (isFloatToInt(op) && !op.endsWith("_min_mag"));
}

function needsExactArg(op) {
  return needsRoundingArg(op) || op.endsWith("_min_mag");
}

function outputKind(op) {
  if (isBoolResult(op)) return "bool";
  if (op.endsWith("_to_ui32")) return "ui32";
  if (op.endsWith("_to_ui64")) return "ui64";
  if (op.endsWith("_to_i32")) return "i32";
  if (op.endsWith("_to_i64")) return "i64";
  if (op.endsWith("_to_ui32_min_mag")) return "ui32";
  if (op.endsWith("_to_ui64_min_mag")) return "ui64";
  if (op.endsWith("_to_i32_min_mag")) return "i32";
  if (op.endsWith("_to_i64_min_mag")) return "i64";
  if (op.endsWith("_to_bf16")) return "bf16";
  if (op.endsWith("_to_f16") || (op.endsWith("_round_to_int") && op.startsWith("f16_"))) return "f16";
  if (op.endsWith("_to_f32")) return "f32";
  if (op.endsWith("_to_f64")) return "f64";
  if (op.startsWith("f16_")) return "f16";
  if (op.startsWith("f32_")) return "f32";
  if (op.startsWith("f64_")) return "f64";
  fail(`unknown output kind for ${op}`);
}

function mbBitsType(fmt) {
  return {
    f16: "UInt16",
    bf16: "UInt16",
    f32: "UInt",
    f64: "UInt64",
    ui32: "UInt",
    i32: "UInt",
    ui64: "UInt64",
    i64: "UInt64",
  }[fmt];
}

function mbInputExpr(kind, value) {
  if (kind === "f16") return `F16::from_bits(${value})`;
  if (kind === "bf16") return `BF16::from_bits(${value})`;
  if (kind === "f32") return `F32::from_bits(${value})`;
  if (kind === "f64") return `F64::from_bits(${value})`;
  if (kind === "ui32") return value;
  if (kind === "ui64") return value;
  if (kind === "i32") return `${value}.reinterpret_as_int()`;
  if (kind === "i64") return `${value}.reinterpret_as_int64()`;
  fail(`unknown input kind ${kind}`);
}

function inputKindForUnaryOp(op, source) {
  if (source.kind === "edge_bf16") return "bf16";
  if (op.startsWith("bf16_")) return "bf16";
  if (Object.hasOwn(source, "inputKind")) return source.inputKind;
  if (Object.hasOwn(source, "input_kind")) return source.input_kind;
  return source.fmt ?? "f16";
}

function mbMixLines(op, inputExprs) {
  if (isPredicate(op)) {
    return [
      `    let value = ${op}(${inputExprs[0]})`,
      "    hash = sweep_mix(hash, if value { 1UL } else { 0UL })",
      "    hash = sweep_mix(hash, 0UL)",
    ];
  }

  let args;
  if (op.endsWith("_min_mag")) {
    args = ["context", inputExprs[0], "exact"];
  } else if (needsRoundingArg(op)) {
    args = ["context", inputExprs[0], "rounding", "exact"];
  } else {
    args = ["context", ...inputExprs];
  }

  const kind = outputKind(op);
  let valueExpr;
  if (kind === "bool") valueExpr = "if result.value { 1UL } else { 0UL }";
  else if (kind === "f16" || kind === "bf16") valueExpr = "result.value.to_bits().to_uint64()";
  else if (kind === "f32") valueExpr = "result.value.to_bits().to_uint64()";
  else if (kind === "f64") valueExpr = "result.value.to_bits()";
  else if (kind === "ui32") valueExpr = "result.value.to_uint64()";
  else if (kind === "ui64") valueExpr = "result.value";
  else if (kind === "i32") valueExpr = "result.value.reinterpret_as_uint().to_uint64()";
  else if (kind === "i64") valueExpr = "result.value.reinterpret_as_uint64()";
  else fail(`unknown output kind ${kind}`);

  return [
    `    let result = ${op}(${args.join(", ")})`,
    `    hash = sweep_mix(hash, ${valueExpr})`,
    "    hash = sweep_mix(hash, result.context.flags.to_bits().to_uint64())",
  ];
}

function mbCommonHelpers() {
  const lines = [
    "///|",
    "fn sweep_mix(hash : UInt64, value : UInt64) -> UInt64 {",
    `  (hash ^ value) * 0x${hex64(HASH_PRIME)}UL`,
    "}",
    "",
    "///|",
    "fn sweep_lcg_next(state : UInt64) -> UInt64 {",
    `  state * 0x${hex64(LCG_MULT)}UL + 0x${hex64(LCG_INC)}UL`,
    "}",
    "",
    "///|",
    "fn sweep_rounding4(index : Int) -> RoundingMode {",
    "  match index {",
    "    0 => NearEven",
    "    1 => Min",
    "    2 => Max",
    "    _ => Odd",
    "  }",
    "}",
    "",
    "///|",
    "fn sweep_rounding6(index : Int) -> RoundingMode {",
    "  match index {",
    "    0 => NearEven",
    "    1 => MinMag",
    "    2 => Min",
    "    3 => Max",
    "    4 => NearMaxMag",
    "    _ => Odd",
    "  }",
    "}",
    "",
    "///|",
    "fn sweep_tininess(index : Int) -> TininessMode {",
    "  if index == 0 { AfterRounding } else { BeforeRounding }",
    "}",
    "",
    "///|",
    "fn sweep_context(spec : Int, rounding : RoundingMode, tininess : TininessMode) -> Context {",
    "  let context = if spec == 0 { Context::default() } else { Context::riscv() }",
    "  context.with_rounding(rounding).with_tininess(tininess)",
    "}",
  ];

  for (const [fmt, values] of Object.entries(EDGE_VALUES)) {
    lines.push("", "///|", `fn sweep_edge_${fmt}(index : Int) -> ${mbBitsType(fmt)} {`, "  match index {");
    values.forEach((value, index) => {
      const suffix = fmt === "f16" ? "" : (fmt === "f32" ? "U" : "UL");
      lines.push(`    ${index} => 0x${value}${suffix}`);
    });
    lines.push("    _ => 0", "  }", "}");
  }

  for (const [kind, values] of Object.entries(INTEGER_INPUTS)) {
    lines.push("", "///|", `fn sweep_edge_${kind}(index : Int) -> ${mbBitsType(kind)} {`, "  match index {");
    const suffix = kind.endsWith("32") ? "U" : "UL";
    values.forEach((value, index) => {
      lines.push(`    ${index} => 0x${value}${suffix}`);
    });
    lines.push("    _ => 0", "  }", "}");
  }
  return lines;
}

function mbDefineRandomBits(lines, fmt, arity) {
  ["a", "b", "c"].slice(0, arity).forEach((name) => {
    lines.push("    rng = sweep_lcg_next(rng)");
    if (fmt === "f16") {
      lines.push(`    let ${name}_bits : UInt16 = (rng & 0xFFFFUL).to_uint16()`);
    } else if (fmt === "f32") {
      lines.push(`    let ${name}_bits : UInt = (rng & 0xFFFFFFFFUL).to_uint()`);
    } else {
      lines.push(`    let ${name}_bits : UInt64 = rng`);
    }
  });
}

function mbCaseLines(digestCase) {
  // This emitter rebuilds the input stream from compact source metadata. The
  // test compares only a digest, but the loop structure exactly mirrors the
  // fixture writer, including random seed advancement and edge-value ordering.
  const source = digestCase.source;
  const specId = digestCase.spec === "x86_8086_sse" ? 0 : 1;
  const fnName = `sweep_digest_${digestCase.name.replaceAll("-", "_")}`;
  const lines = ["", "///|", `fn ${fnName}() -> UInt64 {`, `  let mut hash = 0x${hex64(HASH_INIT)}UL`];
  const kind = String(source.kind);

  if (kind === "random") {
    const fmt = String(source.fmt);
    const arity = Number(source.arity);
    const count = Number(source.count);
    lines.push(`  let mut rng = 0x${hex64(source.seed)}UL`);
    lines.push(`  for index in 0..<${count} {`);
    lines.push("    let rounding = sweep_rounding4(index % 4)");
    lines.push("    let tininess = sweep_tininess((index / 4) % 2)");
    if (needsExactArg(digestCase.op)) lines.push("    let exact = true");
    if (!isPredicate(digestCase.op)) lines.push(`    let context = sweep_context(${specId}, rounding, tininess)`);
    mbDefineRandomBits(lines, fmt, arity);
    const inputs = ["a", "b", "c"].slice(0, arity).map((name) => mbInputExpr(fmt, `${name}_bits`));
    lines.push(...mbMixLines(digestCase.op, inputs));
    lines.push("  }");
  } else if (kind === "edge_binary" || kind === "edge_fma") {
    const fmt = String(source.fmt);
    const edgeLen = EDGE_VALUES[fmt].length;
    const arity = kind === "edge_binary" ? 2 : 3;
    const valueCount = edgeLen * edgeLen;
    const total = valueCount * ROUNDINGS.length * TININESS.length;
    lines.push(`  for index in 0..<${total} {`);
    lines.push(`    let mode_index = index / ${valueCount}`);
    lines.push(`    let input_index = index % ${valueCount}`);
    lines.push("    let rounding = sweep_rounding4(mode_index / 2)");
    lines.push("    let tininess = sweep_tininess(mode_index % 2)");
    lines.push(`    let context = sweep_context(${specId}, rounding, tininess)`);
    lines.push(`    let a_bits = sweep_edge_${fmt}(input_index / ${edgeLen})`);
    lines.push(`    let b_bits = sweep_edge_${fmt}(input_index % ${edgeLen})`);
    if (arity === 3) {
      lines.push(`    let c_bits = sweep_edge_${fmt}(((input_index / ${edgeLen}) * 7 + (input_index % ${edgeLen}) * 3) % ${edgeLen})`);
    }
    const inputs = ["a", "b", "c"].slice(0, arity).map((name) => mbInputExpr(fmt, `${name}_bits`));
    lines.push(...mbMixLines(digestCase.op, inputs));
    lines.push("  }");
  } else if (kind === "edge_unary_all_rounding" || kind === "edge_int_all_rounding") {
    const inputKind = inputKindForUnaryOp(digestCase.op, source);
    const count = Object.hasOwn(INTEGER_INPUTS, inputKind) ? INTEGER_INPUTS[inputKind].length : EDGE_VALUES[inputKind].length;
    const total = count * ALL_ROUNDINGS.length * 2;
    lines.push(`  for index in 0..<${total} {`);
    lines.push(`    let mode_index = index / ${count}`);
    lines.push(`    let input_index = index % ${count}`);
    lines.push("    let rounding = sweep_rounding6(mode_index / 2)");
    lines.push("    let tininess = AfterRounding");
    if (needsExactArg(digestCase.op)) lines.push("    let exact = mode_index % 2 == 1");
    if (!isPredicate(digestCase.op)) lines.push(`    let context = sweep_context(${specId}, rounding, tininess)`);
    lines.push(`    let a_bits = sweep_edge_${inputKind}(input_index)`);
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr(inputKind, "a_bits")]));
    lines.push("  }");
  } else if (kind === "edge_unary_min_mag") {
    const fmt = String(source.fmt);
    const count = EDGE_VALUES[fmt].length;
    const total = count * 2;
    lines.push(`  for index in 0..<${total} {`);
    lines.push(`    let input_index = index % ${count}`);
    lines.push(`    let context = sweep_context(${specId}, NearEven, AfterRounding)`);
    lines.push(`    let exact = index / ${count} == 1`);
    lines.push(`    let a_bits = sweep_edge_${fmt}(input_index)`);
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr(fmt, "a_bits")]));
    lines.push("  }");
  } else if (kind === "edge_unary_all_mode") {
    const fmt = String(source.fmt);
    const count = EDGE_VALUES[fmt].length;
    const total = count * ROUNDINGS.length * TININESS.length;
    lines.push(`  for index in 0..<${total} {`);
    lines.push(`    let mode_index = index / ${count}`);
    lines.push(`    let input_index = index % ${count}`);
    lines.push("    let rounding = sweep_rounding4(mode_index / 2)");
    lines.push("    let tininess = sweep_tininess(mode_index % 2)");
    lines.push(`    let context = sweep_context(${specId}, rounding, tininess)`);
    lines.push(`    let a_bits = sweep_edge_${fmt}(input_index)`);
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr(fmt, "a_bits")]));
    lines.push("  }");
  } else if (kind === "edge_bf16") {
    const count = EDGE_VALUES.f16.length;
    lines.push(`  for index in 0..<${count} {`);
    if (!isPredicate(digestCase.op)) lines.push(`    let context = sweep_context(${specId}, NearEven, AfterRounding)`);
    lines.push("    let a_bits = sweep_edge_f16(index)");
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr("bf16", "a_bits")]));
    lines.push("  }");
  } else if (kind === "exhaustive_f16_unary") {
    lines.push("  for index in 0..<0x10000 {");
    if (!isPredicate(digestCase.op)) lines.push(`    let context = sweep_context(${specId}, NearEven, AfterRounding)`);
    lines.push("    let a_bits = index.to_uint16()");
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr("f16", "a_bits")]));
    lines.push("  }");
  } else if (kind === "exhaustive_bf16_unary") {
    lines.push("  for index in 0..<0x10000 {");
    if (!isPredicate(digestCase.op)) lines.push(`    let context = sweep_context(${specId}, NearEven, AfterRounding)`);
    lines.push("    let a_bits = index.to_uint16()");
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr("bf16", "a_bits")]));
    lines.push("  }");
  } else if (kind === "exhaustive_f16_round_to_int") {
    lines.push("  for index in 0..<(0x10000 * 4) {");
    lines.push("    let rounding = sweep_rounding4(index / 0x10000)");
    lines.push(`    let context = sweep_context(${specId}, rounding, AfterRounding)`);
    lines.push("    let exact = true");
    lines.push("    let a_bits = (index % 0x10000).to_uint16()");
    lines.push(...mbMixLines(digestCase.op, [mbInputExpr("f16", "a_bits")]));
    lines.push("  }");
  } else {
    fail(`unknown sweep source kind ${kind}`);
  }

  lines.push(
    "  hash",
    "}",
    "",
    "///|",
    `test "golden sweep digest ${digestCase.name}" {`,
    `  @debug.assert_eq(${fnName}(), 0x${hex64(digestCase.digest)}UL)`,
    "}",
  );
  return lines;
}

function emitDigestTests() {
  const lines = [GENERATED_HEADER, ""];
  lines.push(...mbCommonHelpers());
  for (const digestCase of DIGEST_CASES) {
    lines.push(...mbCaseLines(digestCase));
  }
  writeText(SWEEP_TEST_PATH, lines.join("\n") + "\n");
}

function parseSweepArgs(argv) {
  const args = {
    quick: false,
    skipEdge: false,
    skipRandom: false,
    skipExhaustive: false,
    f16BinaryCount: 65536,
    f32BinaryCount: 10000,
    f64BinaryCount: 10000,
    fmaCount: 10000,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const option = argv[index];
    if (option === "--quick") args.quick = true;
    else if (option === "--skip-edge") args.skipEdge = true;
    else if (option === "--skip-random") args.skipRandom = true;
    else if (option === "--skip-exhaustive") args.skipExhaustive = true;
    else if (option === "--f16-binary-count") args.f16BinaryCount = Number(argv[++index]);
    else if (option === "--f32-binary-count") args.f32BinaryCount = Number(argv[++index]);
    else if (option === "--f64-binary-count") args.f64BinaryCount = Number(argv[++index]);
    else if (option === "--fma-count") args.fmaCount = Number(argv[++index]);
    else fail(`unknown sweep option: ${option}`);
  }
  if (args.quick) {
    args.f16BinaryCount = 32;
    args.f32BinaryCount = 32;
    args.f64BinaryCount = 32;
    args.fmaCount = 32;
    args.skipExhaustive = true;
  }
  for (const [key, value] of Object.entries(args)) {
    if (key !== "quick" && key.startsWith("skip") === false && typeof value === "number" && !Number.isInteger(value)) {
      fail(`${key} must be an integer`);
    }
  }
  return args;
}

function generateSweepVectors(argv = [], { writePackages = true } = {}) {
  const args = parseSweepArgs(argv);
  ensureOracles();
  ensureDir(VECTOR_DIR);
  DIGEST_CASES.length = 0;
  cleanGeneratedSweepFixtures();
  if (!args.skipEdge) generateEdgeVectors();
  if (!args.skipRandom) generateRandomVectors(args);
  if (!args.skipExhaustive) {
    generateExhaustiveBf16Vectors();
    generateExhaustiveF16Vectors();
  }
  emitDigestTests();
  if (writePackages) generateVectorPackages();
  if (commandExists("wc")) {
    runCommand(["wc", "-l", ...listVectorFiles()], { stdio: "inherit" });
  }
}

