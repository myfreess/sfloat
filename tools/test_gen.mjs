function mbRounding(name) {
  return {
    near_even: "NearEven",
    min_mag: "MinMag",
    min: "Min",
    max: "Max",
    near_max_mag: "NearMaxMag",
    odd: "Odd",
  }[name];
}

function mbTininess(name) {
  return { before: "BeforeRounding", after: "AfterRounding" }[name];
}

function mbContext(spec, rounding, tininess) {
  const base = spec === "x86_8086_sse" ? "Context::default()" : "Context::riscv()";
  return `${base}.with_rounding(${mbRounding(rounding)}).with_tininess(${mbTininess(tininess)})`;
}

function mbInput(kind, value, qualified = false) {
  const prefix = qualified ? "@sf." : "";
  if (kind === "f16") return `${prefix}F16::from_bits(0x${value})`;
  if (kind === "bf16") return `${prefix}BF16::from_bits(0x${value})`;
  if (kind === "f32") return `${prefix}F32::from_bits(0x${value}U)`;
  if (kind === "f64") return `${prefix}F64::from_bits(0x${value}UL)`;
  if (kind === "ui32") return `0x${value}U`;
  if (kind === "ui64") return `0x${value}UL`;
  if (kind === "i32") return `0x${value}U.reinterpret_as_int()`;
  if (kind === "i64") return `0x${value}UL.reinterpret_as_int64()`;
  fail(`unknown input kind ${kind}`);
}

function mbExpected(kind, value) {
  if (kind === "f16" || kind === "bf16") return `0x${value}`;
  if (kind === "f32") return `0x${value}U`;
  if (kind === "f64") return `0x${value}UL`;
  if (kind === "ui32") return `0x${value}U`;
  if (kind === "ui64") return `0x${value}UL`;
  if (kind === "i32") return `0x${value}U.reinterpret_as_int()`;
  if (kind === "i64") return `0x${value}UL.reinterpret_as_int64()`;
  if (kind === "bool") return value === "1" ? "true" : "false";
  fail(`unknown expected kind ${kind}`);
}

function coreCallExpr(coreCase, spec) {
  const context = mbContext(spec, coreCase.rounding, coreCase.tininess);
  const inputs = coreCase.inputKinds.map((kind, index) => mbInput(kind, coreCase.inputs[index]));
  const exact = coreCase.exact === "1" ? "true" : "false";
  const op = coreCase.op;

  if (op.endsWith("_is_signaling_nan")) return `${op}(${inputs[0]})`;
  if (op.endsWith("_round_to_int")) {
    return `${op}(${context}, ${inputs[0]}, ${mbRounding(coreCase.rounding)}, ${exact})`;
  }
  if (op.includes("_to_")
    && (op.endsWith("ui32") || op.endsWith("ui64") || op.endsWith("i32") || op.endsWith("i64"))) {
    return `${op}(${context}, ${inputs[0]}, ${mbRounding(coreCase.rounding)}, ${exact})`;
  }
  if (op.endsWith("_min_mag")) return `${op}(${context}, ${inputs[0]}, ${exact})`;
  return `${op}(${context}, ${inputs.join(", ")})`;
}

function emitCoreTest(records) {
  const lines = [
    "///|",
    'test "golden core vectors match committed C oracle records" {',
  ];
  records.forEach(([coreCase, record], index) => {
    const fields = record.split(/\s+/);
    const spec = fields[0];
    const output = fields.at(-2);
    const flags = fields.at(-1);
    const expr = coreCallExpr(coreCase, spec);
    lines.push(`  // ${record}`);
    lines.push(`  let result${index} = ${expr}`);
    if (coreCase.outputKind === "bool" && coreCase.op.endsWith("_is_signaling_nan")) {
      lines.push(`  @debug.assert_eq(result${index}, ${mbExpected(coreCase.outputKind, output)})`);
      lines.push(`  @debug.assert_eq(0U, 0x${flags}U)`);
    } else {
      const valueExpr = ["f16", "bf16", "f32", "f64"].includes(coreCase.outputKind)
        ? `result${index}.value.to_bits()`
        : `result${index}.value`;
      lines.push(`  @debug.assert_eq(${valueExpr}, ${mbExpected(coreCase.outputKind, output)})`);
      lines.push(`  @debug.assert_eq(result${index}.context.flags.to_bits(), 0x${flags}U)`);
    }
  });
  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function generateCoreVectors({ writePackages = true } = {}) {
  const records = [];
  for (const coreCase of coreCases()) {
    for (const spec of SPECS) {
      records.push([coreCase, runOracle(spec, coreCase)]);
    }
  }
  writeText(CORE_VECTOR_PATH, records.map(([, record]) => record).join("\n") + "\n");
  writeText(CORE_TEST_PATH, emitCoreTest(records));
  if (writePackages) generateVectorPackages();
}

function allOps() {
  const ops = new Set();
  for (const group of Object.values(BINARY_OPS)) group.forEach((op) => ops.add(op));
  Object.values(FMA_OPS).forEach((op) => ops.add(op));
  Object.values(ROUND_TO_INT_OPS).forEach((op) => ops.add(op));
  for (const group of Object.values(FLOAT_TO_INT_OPS)) group.forEach((op) => ops.add(op));
  for (const group of Object.values(MIN_MAG_OPS)) group.forEach((op) => ops.add(op));
  for (const group of Object.values(FLOAT_CONVERSION_OPS)) group.forEach((op) => ops.add(op));
  INT_TO_FLOAT_OPS.forEach(([, op]) => ops.add(op));
  ops.add("bf16_to_f32");
  ops.add("bf16_is_signaling_nan");
  for (const fmt of ["f16", "f32", "f64"]) {
    [
      `${fmt}_sqrt`,
      `${fmt}_eq`,
      `${fmt}_le`,
      `${fmt}_lt`,
      `${fmt}_eq_signaling`,
      `${fmt}_le_quiet`,
      `${fmt}_lt_quiet`,
      `${fmt}_is_signaling_nan`,
    ].forEach((op) => ops.add(op));
  }
  return ops;
}

const OP_RE = new RegExp(`\\b(${[...allOps()].sort((a, b) => b.length - a.length).map(escapeRegex).join("|")})\\(`, "g");

function escapeRegex(text) {
  return text.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function lineCounts() {
  const counts = {};
  for (const file of listVectorFiles()) {
    const content = readText(file);
    counts[stem(file)] = content.length === 0 ? 0 : content.split(/\r?\n/).filter((line, index, lines) => index < lines.length - 1 || line.length > 0).length;
  }
  return counts;
}

function addSource(sources, prefix, spec, op, source) {
  sources[`${prefix}_${op}_${spec}`] = [op, source];
}

function advanceLcg(rng, count, arity) {
  for (let index = 0; index < count * arity; index += 1) {
    rng.next();
  }
}

function expectedSweepSources(counts) {
  // Package generation works from committed text fixtures. This reconstructs
  // enough source metadata from fixture names and line counts to emit range
  // digest functions without regenerating the fixture files themselves.
  const sources = {};
  for (const spec of SPECS) {
    for (const [fmt, ops] of Object.entries(BINARY_OPS)) {
      for (const op of ops) addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_binary", fmt });
    }
    for (const [fmt, op] of Object.entries(FMA_OPS)) {
      addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_fma", fmt });
    }
    for (const [fmt, ops] of Object.entries(FLOAT_TO_INT_OPS)) {
      for (const op of ops) addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_unary_all_rounding", fmt });
    }
    for (const [fmt, ops] of Object.entries(MIN_MAG_OPS)) {
      for (const op of ops) addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_unary_min_mag", fmt });
    }
    for (const [fmt, op] of Object.entries(ROUND_TO_INT_OPS)) {
      addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_unary_all_rounding", fmt });
    }
    for (const [fmt, ops] of Object.entries(FLOAT_CONVERSION_OPS)) {
      for (const op of ops) addSource(sources, `edge_${fmt}`, spec, op, { kind: "edge_unary_all_mode", fmt });
    }
    for (const [inputKind, op] of INT_TO_FLOAT_OPS) {
      addSource(sources, `edge_${inputKind}`, spec, op, { kind: "edge_int_all_rounding", inputKind });
    }
    for (const op of ["bf16_to_f32", "bf16_is_signaling_nan"]) {
      addSource(sources, "edge_bf16", spec, op, { kind: "edge_bf16" });
    }
  }

  const rng = new Lcg(SEED);
  const defaultCounts = { f16: 65536, f32: 10000, f64: 10000 };
  for (const spec of SPECS) {
    for (const [fmt, ops] of Object.entries(BINARY_OPS)) {
      for (const op of ops) {
        const name = `random_${fmt}_${op}_${spec}`;
        const count = counts[name] ?? defaultCounts[fmt];
        if (Object.hasOwn(counts, name)) {
          addSource(sources, `random_${fmt}`, spec, op, { kind: "random", fmt, arity: 2, count, seed: rng.state });
        }
        advanceLcg(rng, count, 2);
      }
    }
    for (const [fmt, op] of Object.entries(FMA_OPS)) {
      const name = `random_${fmt}_${op}_${spec}`;
      const count = counts[name] ?? 10000;
      if (Object.hasOwn(counts, name)) {
        addSource(sources, `random_${fmt}`, spec, op, { kind: "random", fmt, arity: 3, count, seed: rng.state });
      }
      advanceLcg(rng, count, 3);
    }
    const name = `random_f32_f32_to_bf16_${spec}`;
    const count = counts[name] ?? 10000;
    if (Object.hasOwn(counts, name)) {
      addSource(sources, "random_f32", spec, "f32_to_bf16", { kind: "random", fmt: "f32", arity: 1, count, seed: rng.state });
    }
    advanceLcg(rng, count, 1);
  }

  for (const spec of SPECS) {
    for (const op of ["bf16_to_f32", "bf16_is_signaling_nan"]) {
      addSource(sources, "exhaustive_bf16", spec, op, { kind: "exhaustive_bf16_unary" });
    }
    for (const op of ["f16_to_f32", "f16_to_f64", "f16_is_signaling_nan"]) {
      addSource(sources, "exhaustive_f16", spec, op, { kind: "exhaustive_f16_unary" });
    }
    addSource(sources, "exhaustive_f16", spec, "f16_round_to_int", { kind: "exhaustive_f16_round_to_int" });
  }
  return sources;
}

function qualifyLine(line) {
  let output = line;
  output = output.replaceAll("Context::", "@sf.Context::");
  output = output.replaceAll("BF16::", "__MBT_BF16_METHOD__");
  output = output.replaceAll("F16::", "@sf.F16::");
  output = output.replaceAll("__MBT_BF16_METHOD__", "@sf.BF16::");
  output = output.replaceAll("F32::", "@sf.F32::");
  output = output.replaceAll("F64::", "@sf.F64::");
  output = output.replaceAll("RoundingMode", "@sf.RoundingMode");
  output = output.replaceAll("TininessMode", "@sf.TininessMode");
  output = output.replaceAll(") -> Context", ") -> @sf.Context");
  output = output.replace(VARIANT_RE, (_, name) => `@sf.RoundingMode::${name}`);
  output = output.replace(TININESS_RE, (_, name) => `@sf.TininessMode::${name}`);
  output = output.replace(OP_RE, (_, op) => `@sf.${op}(`);
  return output;
}

function qualify(lines) {
  return lines.map((line) => qualifyLine(line));
}

function publicTestHelpers(lines) {
  return lines.map((line) => line.startsWith("fn sweep_") ? line.replace("fn sweep_", "pub fn sweep_") : line);
}

function readRecords(file) {
  return readText(file)
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line.length > 0);
}

function inputKinds(op) {
  if (op.startsWith("ui32_to_")) return ["ui32"];
  if (op.startsWith("ui64_to_")) return ["ui64"];
  if (op.startsWith("i32_to_")) return ["i32"];
  if (op.startsWith("i64_to_")) return ["i64"];
  if (op.startsWith("bf16_")) return ["bf16"];
  const fmt = op.slice(0, 3);
  if (op.endsWith("_mul_add")) return [fmt, fmt, fmt];
  if (op.endsWith("_add")
    || op.endsWith("_sub")
    || op.endsWith("_mul")
    || op.endsWith("_div")
    || op.endsWith("_rem")
    || (isBoolResult(op) && !isPredicate(op))) {
    return [fmt, fmt];
  }
  return [fmt];
}

function directCall(op, spec, rounding, tininess, exact, inputs) {
  const context = `sweep_context(${spec === "x86_8086_sse" ? 0 : 1}, ${ROUNDING[rounding]}, ${TININESS_MODE[tininess]})`;
  const exactValue = exact === "1" ? "true" : "false";
  if (isPredicate(op)) return `@sf.${op}(${inputs[0]})`;
  if (op.endsWith("_min_mag")) return `@sf.${op}(${context}, ${inputs[0]}, ${exactValue})`;
  if (needsRoundingArg(op)) return `@sf.${op}(${context}, ${inputs[0]}, ${ROUNDING[rounding]}, ${exactValue})`;
  return `@sf.${op}(${context}, ${inputs.join(", ")})`;
}

function directRecordTest(file) {
  const records = readRecords(file);
  const lines = [
    "",
    "///|",
    `test "golden vector records ${stem(file)}" {`,
  ];
  records.forEach((record, index) => {
    const fields = record.split(/\s+/);
    const [spec, op, rounding, tininess, exact] = fields.slice(0, 5);
    const kinds = inputKinds(op);
    const rawInputs = fields.slice(5, 5 + kinds.length);
    const output = fields.at(-2);
    const flags = fields.at(-1);
    const inputs = kinds.map((kind, i) => mbInput(kind, rawInputs[i], true));
    const expr = directCall(op, spec, rounding, tininess, exact, inputs);
    const kind = outputKind(op);
    lines.push(`  // ${record}`);
    lines.push(`  let result${index} = ${expr}`);
    if (isPredicate(op)) {
      lines.push(`  @debug.assert_eq(result${index}, ${mbExpected(kind, output)})`);
      lines.push(`  @debug.assert_eq(0U, 0x${flags}U)`);
    } else if (kind === "bool") {
      lines.push(`  @debug.assert_eq(result${index}.value, ${mbExpected(kind, output)})`);
      lines.push(`  @debug.assert_eq(result${index}.context.flags.to_bits(), 0x${flags}U)`);
    } else if (["f16", "bf16", "f32", "f64"].includes(kind)) {
      lines.push(`  @debug.assert_eq(result${index}.value.to_bits(), ${mbExpected(kind, output)})`);
      lines.push(`  @debug.assert_eq(result${index}.context.flags.to_bits(), 0x${flags}U)`);
    } else {
      lines.push(`  @debug.assert_eq(result${index}.value, ${mbExpected(kind, output)})`);
      lines.push(`  @debug.assert_eq(result${index}.context.flags.to_bits(), 0x${flags}U)`);
    }
  });
  lines.push("}");
  return new GeneratedTest(null, lines);
}

function sweepFunctionName(file) {
  return `sweep_digest_${stem(file).replaceAll("-", "_")}`;
}

function sweepRangeFunction(file, source) {
  const [op, sourceInfo] = source;
  const records = readRecords(file);
  const digestCase = new DigestCase({
    name: stem(file),
    spec: stem(file).endsWith("_x86_8086_sse") ? "x86_8086_sse" : "riscv",
    op,
    source: sourceInfo,
    digest: digestRecords(records),
  });
  const fnName = sweepFunctionName(file);
  const rangeFnName = `${fnName}_range`;
  const fullLines = qualify(mbCaseLines(digestCase));
  const testIndex = fullLines.findIndex((line) => line.startsWith("test "));
  const functionLines = fullLines.slice(0, testIndex - 1);
  const output = [];
  let insertedRandomSkip = false;
  for (const line of functionLines) {
    if (line === `fn ${fnName}() -> UInt64 {`) {
      output.push(`fn ${rangeFnName}(start : Int, end : Int) -> UInt64 {`);
    } else if (line.startsWith("  for index in 0..<")) {
      if (sourceInfo.kind === "random" && !insertedRandomSkip) {
        const arity = Number(sourceInfo.arity);
        output.push(`  for _ in 0..<(start * ${arity}) {`);
        output.push("    rng = sweep_lcg_next(rng)");
        output.push("  }");
        insertedRandomSkip = true;
      }
      output.push("  for index in start..<end {");
    } else {
      output.push(line);
    }
  }
  return output;
}

function sweepDigestTests(file, source) {
  const records = readRecords(file);
  const fnName = sweepFunctionName(file);
  const tests = [];
  for (let chunkIndex = 0, start = 0; start < records.length; chunkIndex += 1, start += DIGEST_CHUNK_SIZE) {
    const end = Math.min(start + DIGEST_CHUNK_SIZE, records.length);
    const digest = digestRecords(records.slice(start, end));
    const lines = [
      "",
      "///|",
      `test "golden sweep digest ${stem(file)} chunk ${chunkIndex}" {`,
      "  @debug.assert_eq(",
      `    ${fnName}_range(${start}, ${end}),`,
      `    0x${hex64(digest)}UL,`,
      "  )",
      "}",
    ];
    tests.push(new GeneratedTest(fnName, lines));
  }
  return [fnName, tests];
}

