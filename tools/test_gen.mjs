#!/usr/bin/env node
// Generate and validate SoftFloat golden-vector tests.
//
// This single entry point intentionally owns the whole golden-vector workflow:
//   - `core` builds the small representative fixture set and direct tests.
//   - `sweep` builds edge/random/exhaustive fixture files and digest tests.
//   - `packages` turns committed fixture files into sweep_test/batch<N> packages.
//   - `check` replays committed fixture files against the C oracle binaries.
//   - `all` refreshes core + sweep + generated MoonBit packages.
//
// Keeping these pieces together matters because the fixture metadata, LCG
// sequence, digest fold, and MoonBit emitters must stay in lockstep. If one
// part changes without the others, the generated sweep packages can silently
// test a different input stream from the one stored under tools/vectors.

