function cleanBatches() {
  ensureDir(TEST_ROOT);
  for (const name of fs.readdirSync(TEST_ROOT)) {
    const file = path.join(TEST_ROOT, name);
    if (name.startsWith("batch") && fs.statSync(file).isDirectory()) {
      fs.rmSync(file, { recursive: true, force: true });
    }
  }
}

function writeBatches(blocks, helpers) {
  cleanBatches();
  const batchCount = Math.ceil(blocks.length / TESTS_PER_BATCH);
  for (let batchIndex = 0; batchIndex < batchCount; batchIndex += 1) {
    const batchDir = path.join(TEST_ROOT, `batch${batchIndex}`);
    ensureDir(batchDir);
    writeText(path.join(batchDir, "moon.pkg"), moonPkg());

    const batchBlocks = blocks.slice(batchIndex * TESTS_PER_BATCH, (batchIndex + 1) * TESTS_PER_BATCH);
    const helperLines = [GENERATED_HEADER, ""];
    helperLines.push(...publicTestHelpers(qualify(mbCommonHelpers())));
    const seenHelpers = new Set();
    for (const block of batchBlocks) {
      if (block.helper !== null && !seenHelpers.has(block.helper)) {
        helperLines.push(...helpers[block.helper]);
        seenHelpers.add(block.helper);
      }
    }
    writeText(path.join(batchDir, "helpers_test.mbt"), helperLines.join("\n").trimEnd() + "\n");

    const fileCount = Math.ceil(batchBlocks.length / TESTS_PER_FILE);
    for (let fileIndex = 0; fileIndex < fileCount; fileIndex += 1) {
      const fileBlocks = batchBlocks.slice(fileIndex * TESTS_PER_FILE, (fileIndex + 1) * TESTS_PER_FILE);
      const lines = [GENERATED_HEADER];
      for (const block of fileBlocks) {
        lines.push(...block.block);
      }
      writeText(path.join(batchDir, `vectors_${String(fileIndex).padStart(3, "0")}_test.mbt`), lines.join("\n").trimEnd() + "\n");
    }
  }
}

function generateVectorPackages() {
  const counts = lineCounts();
  const sources = expectedSweepSources(counts);
  const blocks = [];
  const helpers = {};
  for (const name of ["core", "smoke"]) {
    const file = path.join(VECTOR_DIR, `${name}.txt`);
    if (exists(file)) {
      blocks.push(directRecordTest(file));
    }
  }
  for (const file of listVectorFiles()) {
    const name = stem(file);
    if (name === "core" || name === "smoke") continue;
    const source = sources[name];
    if (source === undefined) {
      fail(`no generated sweep source metadata for ${path.basename(file)}`);
    }
    const [helperName, tests] = sweepDigestTests(file, source);
    helpers[helperName] = sweepRangeFunction(file, source);
    blocks.push(...tests);
  }
  writeBatches(blocks, helpers);
}

function checkVectorGroups() {
  ensureOracles();
  const groups = new Map();
  for (const file of listVectorFiles()) {
    const lines = readText(file).split(/\r?\n/);
    lines.forEach((rawLine, offset) => {
      const line = rawLine.trim();
      if (line.length === 0 || line.startsWith("#")) return;
      const fields = line.split(/\s+/);
      if (fields.length < 7) {
        fail(`${file}:${offset + 1}: malformed vector record`);
      }
      const [spec, op, rounding, tininess, exact] = fields.slice(0, 5);
      if (!Object.hasOwn(ORACLES, spec)) {
        fail(`${file}:${offset + 1}: unknown specialization ${spec}`);
      }
      const key = `${spec}\0${op}\0${rounding}\0${tininess}\0${exact}`;
      if (!groups.has(key)) {
        groups.set(key, []);
      }
      groups.get(key).push({
        file,
        lineNumber: offset + 1,
        line,
        inputs: fields.slice(5, -2),
      });
    });
  }

  for (const key of [...groups.keys()].sort()) {
    const [spec, op, rounding, tininess, exact] = key.split("\0");
    const records = groups.get(key);
    const input = records.map((record) => record.inputs.join(" ")).join("\n") + "\n";
    const completed = runCommand(
      [oraclePath(spec), "--batch", op, rounding, tininess, exact],
      { input, captureOutput: true },
    );
    const actualLines = completed.stdout.split(/\r?\n/).filter((line) => line.length > 0);
    if (actualLines.length !== records.length) {
      fail(`${op}/${spec}: expected ${records.length} records, got ${actualLines.length}`);
    }
    records.forEach((record, index) => {
      const actual = actualLines[index];
      if (actual !== record.line) {
        fail([
          `vector mismatch in ${record.file}:${record.lineNumber}`,
          `expected: ${record.line}`,
          `actual:   ${actual}`,
        ].join("\n"));
      }
    });
  }
}

function commandExists(command) {
  const result = spawnSync("command", ["-v", command], { shell: true, encoding: "utf8" });
  return result.status === 0;
}

function printHelp() {
  process.stdout.write(`Usage: tools/test_gen.mjs <command> [options]

Commands:
  core       Generate tools/vectors/core.txt and golden_core_wbtest.mbt, then rebuild sweep_test.
  sweep      Generate edge/random/exhaustive vectors and golden_sweep_digest_wbtest.mbt, then rebuild sweep_test.
  packages   Rebuild sweep_test/batch<N> packages from existing tools/vectors fixtures.
  check      Replay all committed fixtures through the local C oracle binaries.
  all        Generate core and sweep fixtures, then rebuild sweep_test once.

Sweep options:
  --quick
  --skip-edge
  --skip-random
  --skip-exhaustive
  --f16-binary-count <n>
  --f32-binary-count <n>
  --f64-binary-count <n>
  --fma-count <n>
`);
}

function main(argv) {
  const [command, ...rest] = argv;
  if (command === "core") {
    generateCoreVectors();
  } else if (command === "sweep") {
    generateSweepVectors(rest);
  } else if (command === "packages") {
    generateVectorPackages();
  } else if (command === "check") {
    checkVectorGroups();
  } else if (command === "all") {
    generateCoreVectors({ writePackages: false });
    generateSweepVectors(rest, { writePackages: false });
    generateVectorPackages();
  } else if (command === undefined || command === "-h" || command === "--help" || command === "help") {
    printHelp();
  } else {
    printHelp();
    fail(`unknown command: ${command}`);
  }
}

try {
  main(process.argv.slice(2));
} catch (error) {
  process.stderr.write(`${error.message}\n`);
  process.exit(1);
}
