function fail(message) {
  throw new Error(message);
}

function runCommand(args, options = {}) {
  const result = spawnSync(args[0], args.slice(1), {
    cwd: ROOT,
    encoding: "utf8",
    // Some validation groups contain hundreds of thousands of oracle records.
    // Node's default 1 MiB spawnSync buffer is too small for those batches,
    // while Python's subprocess path used an effectively larger pipe.
    maxBuffer: 512 * 1024 * 1024,
    ...options,
  });
  if (result.status !== 0) {
    const command = args.join(" ");
    const stderr = result.stderr ? `\n${result.stderr.trimEnd()}` : "";
    const stdout = result.stdout ? `\n${result.stdout.trimEnd()}` : "";
    fail(`command failed (${result.status ?? "signal"}): ${command}${stdout}${stderr}`);
  }
  return result;
}

